import numpy as np


def main():
    sig_1 = np.array([[0.+0.j, 1.+0.j], [1.+0.j, 0.+0.j]])
    sig_2 = np.array([[0.+0.j, 0.-1.j], [0.+1.j, 0.+0.j]])
    sig_3 = np.array([[1.+0.j, 0.+0.j], [0.+0.j, -1.+0.j]])

    print(sig_1)
    print(sig_2)
    print(sig_3)

    l = list()

    s12 = np.kron(sig_1, sig_2)
    l.append(s12)
    s13 = np.kron(sig_1, sig_3)
    l.append(s13)
    s23 = np.kron(sig_2, sig_3)
    l.append(s23)
    s32 = np.kron(sig_3, sig_2)
    l.append(s32)
    s31 = np.kron(sig_3, sig_1)
    l.append(s31)
    s21 = np.kron(sig_2, sig_1)
    l.append(s21)

    for item in l:
        print(item)


if __name__ == "__main__":
    main()


